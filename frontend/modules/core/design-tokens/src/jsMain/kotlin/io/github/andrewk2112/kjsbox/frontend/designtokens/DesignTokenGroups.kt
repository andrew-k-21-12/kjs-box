package io.github.andrewk2112.kjsbox.frontend.designtokens

/**
 * Marker interface to denote a group of context-independent design tokens.
 */
interface DesignTokenGroup

/**
 * Sizes related to fonts.
 */
interface FontSizes : DesignTokenGroup

/**
 * Opacity values.
 */
interface Opacities : DesignTokenGroup

/**
 * Color palette.
 */
interface Palette : DesignTokenGroup

/**
 * Corner and shadow radius values.
 */
interface Radii : DesignTokenGroup

/**
 * Various element sizes.
 */
interface Sizes : DesignTokenGroup

/**
 * Margin, padding values.
 */
interface Spacing : DesignTokenGroup

/**
 * Time duration values.
 */
interface Time : DesignTokenGroup

/**
 * Transition timing configurations.
 */
interface Timing : DesignTokenGroup
